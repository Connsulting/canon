func TestRenderDefaultsToAIRenderAndWritesStateFolder(t *testing.T) {
	root := t.TempDir()
	if err := canon.EnsureLayout(root, true); err != nil {
		t.Fatalf("EnsureLayout failed: %v", err)
	}

	if _, err := canon.Ingest(root, canon.IngestInput{
		Text:   "Billing retries must happen daily.",
		Title:  "Billing Retry",
		Domain: "billing",
	}); err != nil {
		t.Fatalf("Ingest failed: %v", err)
	}

	binDir := t.TempDir()
	codexPath := filepath.Join(binDir, "codex")
	script := `#!/bin/sh
out=""
while [ "$#" -gt 0 ]; do
  if [ "$1" = "-o" ]; then
    out="$2"
    shift 2
    continue
  fi
  shift
done

if [ -z "$out" ]; then
  echo "missing output path" >&2
  exit 2
fi

cat >/dev/null

cat <<'JSON' > "$out"
{
  "model": "codex-headless",
  "domain_docs": [
    {
      "domain": "billing",
      "body": "<!-- AUTO-GENERATED by canon. Do not edit directly. -->\n<!-- Domain: billing -->\n<!-- Contributing specs: spec-000 -->\n\n# BILLING\n\n## Effective State\n\n- AI-effective state for billing.\n"
    }
  ],
  "interaction_docs": [],
  "gaps": "<!-- AUTO-GENERATED by canon. Do not edit directly. -->\n# Known Gaps\n\nNo unresolved gaps.\n"
}
JSON
`
	if err := os.WriteFile(codexPath, []byte(script), 0o755); err != nil {
		t.Fatalf("failed writing fake codex binary: %v", err)
	}

	t.Setenv("PATH", binDir+string(os.PathListSeparator)+os.Getenv("PATH"))
	t.Setenv("HOME", t.TempDir())

	if err := run([]string{"render", "--root", root, "--write"}); err != nil {
		t.Fatalf("render command failed: %v", err)
	}

	billingState, err := os.ReadFile(filepath.Join(root, "state", "billing.md"))
	if err != nil {
		t.Fatalf("failed reading billing state file: %v", err)
	}
	if !strings.Contains(string(billingState), "AI-effective state for billing.") {
		t.Fatalf("expected AI render content in state/billing.md")
	}

	if _, err := os.Stat(filepath.Join(root, "state2")); err == nil {
		t.Fatalf("did not expect render output in state2/")
	} else if !os.IsNotExist(err) {
		t.Fatalf("failed checking state2 folder: %v", err)
	}
}

func TestLogGraphFlagsRenderDependencyGraph(t *testing.T) {
	root := t.TempDir()
	if err := canon.EnsureLayout(root, true); err != nil {
		t.Fatalf("EnsureLayout failed: %v", err)
	}

	if _, err := canon.Ingest(root, canon.IngestInput{
		Text:   "Base auth requirement.",
		ID:     "spec-a",
		Title:  "Auth Base",
		Type:   "feature",
		Domain: "auth",
	}); err != nil {
		t.Fatalf("Ingest spec-a failed: %v", err)
	}
	if _, err := canon.Ingest(root, canon.IngestInput{
		Text:      "API depends on auth.",
		ID:        "spec-b",
		Title:     "Rate Limits",
		Type:      "feature",
		Domain:    "api",
		DependsOn: []string{"spec-a"},
	}); err != nil {
		t.Fatalf("Ingest spec-b failed: %v", err)
	}

	out := captureStdout(t, func() {
		if err := run([]string{"log", "--root", root, "--graph", "--oneline", "--all"}); err != nil {
			t.Fatalf("log command failed: %v", err)
		}
	})

	if !strings.Contains(out, "* spec-b") {
		t.Fatalf("expected spec-b graph node, got:\n%s", out)
	}
	if !strings.Contains(out, "* spec-a") {
		t.Fatalf("expected spec-a graph node, got:\n%s", out)
	}
	if strings.Contains(out, "|->") {
		t.Fatalf("did not expect separate dependency edge lines, got:\n%s", out)
	}
}

func captureStdout(t *testing.T, fn func()) string {
	t.Helper()

	originalStdout := os.Stdout
	reader, writer, err := os.Pipe()
	if err != nil {
		t.Fatalf("os.Pipe failed: %v", err)
	}

	os.Stdout = writer
	defer func() {
		os.Stdout = originalStdout
	}()

	fn()

	if err := writer.Close(); err != nil {
		t.Fatalf("closing writer failed: %v", err)
	}
	out, err := io.ReadAll(reader)
	if err != nil {
		t.Fatalf("reading captured output failed: %v", err)
	}
	if err := reader.Close(); err != nil {
		t.Fatalf("closing reader failed: %v", err)
	}
	return string(out)
}

func TestResetCommandResetsToReferenceSpec(t *testing.T) {
	root := t.TempDir()
	if err := canon.EnsureLayout(root, true); err != nil {
		t.Fatalf("EnsureLayout failed: %v", err)
	}

	if _, err := canon.Ingest(root, canon.IngestInput{
		Text: `---
id: spec-501
type: feature
title: "Initial Policy"
domain: api
created: 2026-02-19T12:00:00Z
depends_on: []
touched_domains: [api]
---
Initial API policy.`,
	}); err != nil {
		t.Fatalf("first ingest failed: %v", err)
	}
	if _, err := canon.Ingest(root, canon.IngestInput{
		Text: `---
id: spec-502
type: feature
title: "Follow Up"
domain: api
created: 2026-02-19T12:10:00Z
depends_on: [spec-501]
touched_domains: [api]
---
Follow-up API policy.`,
	}); err != nil {
		t.Fatalf("second ingest failed: %v", err)
	}

	if err := run([]string{"reset", "--root", root, "spec-501"}); err != nil {
		t.Fatalf("reset command failed: %v", err)
	}

	if _, err := os.Stat(filepath.Join(root, ".canon", "specs", "spec-502.spec.md")); !os.IsNotExist(err) {
		t.Fatalf("expected spec-502 to be removed, err=%v", err)
	}
	entries, err := canon.LoadLedger(root)
	if err != nil {
		t.Fatalf("LoadLedger failed: %v", err)
	}
	if len(entries) != 1 {
		t.Fatalf("expected 1 ledger entry after reset, got %d", len(entries))
	}
	if entries[0].SpecID != "spec-501" {
		t.Fatalf("expected remaining ledger entry spec-501, got %s", entries[0].SpecID)
	}
}
